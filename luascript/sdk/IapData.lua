function IapData.GetIapItem(channel, productId)
	if IapData[channel] ~= nil and IapData[channel][productId] ~= nil then
		return IapData[channel][productId]
	end

	return nil
end

function IapData.GetProductIdStr(channel)
	local datas = IapData[channel]

	if datas ~= nil then
		local str = ""

		for key, value in pairs(datas) do
			if value.sell == 0 or value.sell == 1 then
				if str ~= "" then
					str = str .. ","
				end

				str = str .. key
			end
		end

		return str
	end

	return ""
end

